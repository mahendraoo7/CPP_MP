#include <iostream>
#include <math.h>
   using namespace std;

int main ()
{
    cout <<"ceil number :"<<ceil(3.6)<<endl;
    cout <<"ceil number :"<<ceil(6.2)<<endl;
    cout <<"ceil number :"<<ceil(4.1)<<endl;

    cout <<endl<<"floor number :"<<floor(3.6)<<endl;
    cout <<"floor number :"<<floor(5.3)<<endl;
    cout <<"floor number :"<<floor(8.9)<<endl;


    cout <<endl<<"Round number :"<<round(5.6)<<endl;
    cout <<"Round number :"<<round(8.9)<<endl;
    cout <<"Round number :"<<round(4.3)<<endl;

    cout <<endl<<"sqrt number :"<<sqrt(81)<<endl;
    cout <<"sqrt number :"<<sqrt(64)<<endl;
    cout <<"sqrt number :"<<sqrt(16)<<endl;
    
    cout <<endl<< "power number :"<<pow(5,4)<<endl;
    cout << "power number :"<<pow(3,4)<<endl;
    cout << "power number :"<<pow(9,2)<<endl;

    cout <<endl<<"abs number :"<<abs(-65)<<endl;
    cout <<"abs number :"<<abs(15)<<endl;
    cout <<"abs number :"<<abs(-87)<<endl;

}